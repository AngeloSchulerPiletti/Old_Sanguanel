                <div class="subject">
                    <p>Um brilhante trabalho escrito por <em>Grady Hull</em>, chamado “<em>Olive Oil Addition 
                    to Yeast as an Alternative to Wort Aeration</em>” estudou a possibilidade de usar 
                    <strong>azeite de oliva</strong> no mosto para evitar a necessidade de <strong>oxigenação</strong> do mesmo. 
                    Importante ressaltar que este estudo foi direcionado à cerveja, mas a ciência 
                    por trás da fermentação é praticamente a mesma. Neste artigo iremos contar um 
                    pouco sobre esse estudo, suas conclusões e daremos um parecer final pessoal.</p>
                </div>

                <div class="subject">
                    <h2>Oxigenação</h2>
                    <p>Antes, entendamos porque é necessário oxigenar o mosto do hidromel. Isso está 
                    intimamente ligado à <a href="/Início/Produção/ComoOHidromelEProduzido">respiração celular das leveduras</a>. De modo simplificado, quando 
                    elas utilizam oxigênio, estão se reproduzindo em número e, quando não estão, ó produto 
                    da reação é o etanol (Álcool). </p>
                    <p>Na fermentação do mosto, antes da fermentação alcoólica acontecer, ocorre a 
                    <strong>respiração aeróbica</strong> onde as células se multiplicam até que o oxigênio se esgote e o 
                    processo de gerar etanol inicie. </p>
                    <p>Para dar às leveduras o oxigênio necessário, o <em>hidromeleiro</em> deve aerar o mosto. 
                    Existem muitas formas de fazê-lo. Desde mexendo com uma colher alimentícia até com a 
                    utilização de oxigênio hospitalar. O problema desse método é a possível <strong>contaminação 
                    </strong>/ou <strong>oxidação do mosto</strong>. </p>
                    <p>É essencial que se entenda a necessidade da respiração aeróbica antes da fermentação 
                    por si só. É na respiração aeróbica que as leveduras sintetizam material celular que 
                    formam importantes <em>organelas</em> (palavra que denomina estruturas de dentro das células) 
                    para a geração de novas células, a principal: a parede celular.</p>
                </div>

