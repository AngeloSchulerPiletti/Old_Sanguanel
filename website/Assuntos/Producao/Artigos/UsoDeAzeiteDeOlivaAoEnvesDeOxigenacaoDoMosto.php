                <div class="subject">
                    <h2>Azeite de Oliva</h2>
                    <p>O trabalho de <em>Grady Hull</em> buscou então uma forma mais segura e eficiente de 
                    prover às leveduras os compostos necessários para a sua multiplicação. Ele 
                    observou que um dos componentes mais importantes para a multiplicação é o <strong>ácido 
                    oleico</strong>. Teve como hipótese a utilização do azeite de oliva, que é rico nessa 
                    substância, para suprir a necessidade de oxigênio.</p>
                    <p>Ele obteve sucesso em seus testes. A fermentação aconteceu e a bebida ficou 
                    sensorialmente degustativa de acordo com o painel sensorial. Os testes eram 
                    feitos com duas amostras, uma com azeite e outra feita por oxigenação (controle). 
                    A amostra de controle demonstrou uma menor produção de <em>off-flavors</em>, em 
                    contrapartida o painel sensorial achou a amostra de controle mais oxidada.</p>
                </div>
                
