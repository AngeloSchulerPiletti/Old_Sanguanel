                    <h2>Como Reproduzir</h2>
                    <p>Caso seja do seu interesse, caro leitor, será explicado a seguir como você 
                    pode testar este método em suas próprias bateladas caseiras. De acordo com os 
                    números do estudo, a dose ideal é de 1mg de azeite para cada 50 bilhões de 
                    células. O que seria entre <b>0,01</b> e <b>0,001ml</b> para cada 20 litros de mosto. Ou 
                    seja, uma quantidade realmente baixa. Para se ter noção, uma gota tem 
                    aproximadamente 0,05ml. </p>
                    <p>Essa quantia deve ser adicionada junto com as leveduras no início da 
                    fermentação.</p>
