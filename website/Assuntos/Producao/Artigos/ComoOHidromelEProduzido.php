                <div class="subject">
                    <h2>Produzindo</h2>
                    <p>Para iniciar a produção, é feito o mosto. Ele é a junção dos ingredientes nas 
                    devidas quantidades. Quando pronto, é colocado no fermentador, que é o local 
                    onde as leveduras irão fermentar o mel e produzir o álcool. A fermentação é 
                    uma etapa delicada, onde um passo em falso pode destruir a batelada. A temperatura 
                    de fermentação, a oxigenação, as bases nitrogenadas, os <em>off-flavors</em> e os 
                    ácidos oleicos são assuntos um pouco mais aprofundados.</p>
                    <p>Esse último processo tende a demorar 10 dias e ao seu término o produto final 
                    deve ser transferido para um novo compartimento onde permanecerá durante a chamada 
                    clarificação. Nessa transferência, deve-se evitar transferir a <em>“lama”</em> do fundo do 
                    balde e também evitar que a bebida entre em muito contato com o oxigênio. Isso 
                    porque pode ocasionar na produção de ácido acético e outros compostos não ideais.</p>
                    <p>O fim da clarificação leva de <strong>3 meses a 10 meses</strong> e costuma ser volátil. Existente 
                    produtos feitos para acelerar esse processo. São os chamados agentes clarificantes.</p>
                </div>

