                <h2>Matérias-Primas Básicas</h2>
                

                <div class="topic">
                    <h3>Mel</h3>
                    <p>O mel é o produto principal do hidromel. Um mel de má procedência com certeza 
                    não dará um bom resultado, ainda assim, não basta ter um mel de boa qualidade. 
                    Se o <em>hidromeleiro</em> não souber casar os sabores, o resultado pode continuar a ser 
                    ruim. </p>
                    <p>Cada mel tem características que combinam com diferentes ingredientes. É como 
                    misturar um bom vinho com uma boa cerveja, o resultado não necessariamente será bom. 
                    O produtor precisa saber harmonizar os ingredientes do hidromel de maneira que 
                    um mel escuro e forte não se sobreponha a ponto de tornar a bebida enjoativa, 
                    assim como um mel claro e frutado não pode ser mascarado pelos aromas do barril 
                    de carvalho.</p>
                    <p>Entre essas habilidades, há uma que não compete ao hidromeleiro, mas sim ao 
                    apicultor. O mel precisa manter suas características para permitir a repetição da 
                    receita do hidromel. Nesse quesito, é importante que a florada se mantenha 
                    semelhante e a genética das abelhas também.</p>
                </div>

