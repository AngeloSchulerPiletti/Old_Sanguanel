                <div class="subject">
                    <p>Basicamente o hidromel é um fermentado assim como a cerveja, o vinho, a cidra 
                    e tantas outras bebidas. Acontece que a diferença entre essas bebidas é enorme e 
                    com o hidromel não poderia ser diferente. Enquanto a cerveja leva cerca de 3 
                    semanas para ficar pronta, o hidromel pode chegar a um ano. Enquanto o vinho 
                    utiliza uma matéria-prima amplamente difundida, a uva, o hidromel usa o valedouro 
                    mel das abelhas. Neste artigo você entenderá de maneira simples como acontece a 
                    produção do <a href="/Início/História/OQueEHidromel">hidromel</a>.</p>
