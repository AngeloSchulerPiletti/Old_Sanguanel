                    <h2>Os indígenas e o Tucanaíra</h2>
                    <p>No nordeste da Amazônia, uma tribo conhecida como Tembés, produzia o hidromel 
                        indígena mais conhecido do Brasil (é um dos poucos que possuem relatos históricos 
                        registrados). O Tucanaíra era produzido a partir do mel das abelhas sem ferrão 
                        encontradas na Amazônia (provavelmente abelhas do gênero melipona) além de ser 
                        acrescido do Saburá. O Saburá nada mais é do que potes de pólen coletados pelas 
                        ASF. Esse pólen acabava tendo função aromática para o hidromel e de nutrição para 
                        as leveduras que precisavam fermentar aquele mel. Isto porque o mel é pobre em 
                        bases nitrogenadas, coisa que o pólen acabava suplementando.</p>
