                <div class="subject">
                    <p>Próximos do enxame, mas não no pito, é possível ver os zangões, são as abelhas machos. 
                        Eles ficam próprios batendo as asas tentando sentir uma abelha princesa (abelha rainha 
                        antes de ser copulada) para acasalarem.</p>
                    <p>Dentre do enxame encontramos 3 estruturas diferentes. Os potes de mel, que são potinhos 
                        feito de cerume (cera e própolis), os potes de pólen, também feitos de cerume, mas 
                        armazenam pólen, os favos de cria, que são favos com alimento larval (um mel especial) 
                        e ovos (postos pela rainha que darão origem às abelhas) e o invólucro que é uma cera 
                        que fica ao redor dos favos de cria para dar estabilidade térmica e proteção.</p>
                </div>

