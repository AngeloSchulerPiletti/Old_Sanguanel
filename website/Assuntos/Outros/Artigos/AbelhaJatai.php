                <div class="subject">
                    <h2>A Matéria-Prima</h2>
                
                <div class="topic">
                    <h3>Cera</h3>
                    <p>A cera é o produto gerado pelas abelhas a partir de glândulas e mel;</p>
                </div>
                <div class="topic">
                    <h3>Própolis</h3>
                    <p>Composto medicinal com função defensiva e estrutural para as abelhas feito a 
                        partir da seiva das plantas;</p>
                </div>
                <div class="topic">
                    <h3>Pólen</h3>
                    <p>É o pólen das plantas desidratado. Serve como alimento proteico e oleico para 
                        as abelhas. Participa da composição do alimento larval;</p>
                </div>
                <div class="topic">
                    <h3>Mel</h3>
                    <p>É o néctar das plantas minimamente modificado e desidratado. É o alimento energético;</p>
                </div>
                </div>   


                <div class="subject" marginup="true">
                    <h2>Como Ajudar?</h2>
                    <p>Existem duas maneiras de ajudar a proteger esse bem natural: </p>
                    <ul>
                        <li>Plantando espécies de plantas nativas no seu quintal;</li>
                        <li>Criando caixas você mesmo, visto que não é necessária nenhuma autorização e 
                            não oferece risco ao produtor;</li>
                    </ul>
                    <p>Vale ressaltar que a criação ou captura dessas abelhas na natureza (via iscas pets) 
                        não só não causa dano algum como é uma atividade estimulada por ONGs e sustentável.</p>
                </div>   

