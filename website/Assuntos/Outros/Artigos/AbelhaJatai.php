                <div class="subject">
                    <h2>Criando As Abelhas</h2>
                    <p>Para começar essa atividade muito interessante, basta que o meliponicultor 
                        (aquele que criar ASFs) adquira sua caixa de abelha sem ferrão de algum 
                        meliponicultor da região. Ao adquirir a caixa é importante se atentar para 
                        os seguintes pontos:</p>
                    <ul>
                        <li>O enxame precisa ser procedente de uma divisão (legislação);</li>
                        <li>O enxame deve ser forte (ver quantidade de abelhas, quantidade de 
                            mel e favos de cria);</li>
                        <li>O enxame não deve ter forídeos (um tipo de praga que mata enxames, são 
                            pequenas mosquinhas)!</li>
                    </ul>
                </div>
                <div class="topic">
                    <p>É sugerível que a primeira caixa de um meliponicultor seja da espécie Jataí, 
                        cuja resistência às intempéries é maior.</p>
                    <p>Ao comprar o enxame escolha o local onde a caixa ficará com precedência. 
                        Depois de colocada no local, um metro que for mexida poderá fazer com que 
                        as abelhas se percam e o enxame definhe (morra). O local de ser na sombra, 
                        fora da chuva e com pelo menos 1m do chão.</p>
                </div>   

                <div class="subject">
                    <h2>Custo</h2>
                    <p>O preço médio de um enxame de jataí forte na caixa é de 100 até 160 reais. 
                        Não pague mais. Escolha sempre de um meliponicultor respeitado e nunca compre 
                        pela internet.</p>
                    <p>Esperamos que essas dicas ajudem você!</p>
