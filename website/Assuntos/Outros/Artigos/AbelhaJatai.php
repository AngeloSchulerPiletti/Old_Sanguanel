            <div class="subject">
                    <p>A abelha jataí é uma espécie de abelha nativa do Brasil que está presente 
                        em praticamente todos os estados do país. Ela é da Tribo dos Meliponinis, 
                        que são abelhas com ferrão atrofiado (por isso o nome abelhas sem ferrão – 
                        ASF). O mel dessa abelha é raríssimo e qualquer quantidade vendida 
                        comercialmente tem um alto valor! Já citamos seu mel na produção de hidromel 
                        pelos jesuítas em <a href="/Início/História/OQueEHidromel">outro artigo</a>. 
                        Neste artigo você conhecerá um pouco mais sobre esse tesouro nacional.</p>
                </div>
                <div class="subject">
                    <h2>As Meliponinis</h2>
                    <p>Muito provavelmente você já as viu por aí. Existem centenas de espécies dessa 
                        tribo. Dentre elas: Jataí (<em>Tetragonisca angustula</em> ou <em>fiebrigi</em>), 
                        Iraí (<em>Nannotrigona testaceicornis</em>), Mandaçaia (<em>Melipona quadrifasciata</em>), 
                        Mandaguari (<em>Scaptotrigona postica</em>), Canudo (<em>Scaptotrigona Depilis</em>), Mirim 
                        (<em>Plebeia emerina</em>),entre tantas outras que seria possível criar um site só para 
                        falar sobre essas abelhas!</p>
                    <p>Elas estão espalhadas por toda América do Sul. Seu mel tem uma tendência mais 
                        ácida e hídrica. É menos doce e permite sentir mais aromas florais, visto que 
                        elas polinizam espécies específicas de plantas. Atualmente é possível encontrar 
                        mel de mandaçaia a venda por um preço de até R$700,00 o kg. Isso se deve à 
                        escassez dessas abelhas e de seu mel. A Jataí, a título de exemplo, produz em 
                        média 900ml de mel ao ano (para comparação, a abelha com ferrão Apis, produz
                        cerca de 40Kg no mesmo período). E inclusive há um 
                        <a href="/Início/Curiosidades/HidromelAbelhaSemFerrao">artigo no site</a> 
                        que fala sobre a produção de hidromel com esse tipo de mel!</p>
                </div>

                <div class="subject">
                    <h2>A Jataí</h2>
                    <p>Também conhecidas no Sul por Alemãozinho é a espécie de abelha sem ferrão (ASF), 
                        mais difundida do Brasil. A entrada característica do seu ninho é de cera branca 
                        ou amarelada quando o enxame está saudável e, preta, quando o enxame está fraco. </p>
                    <p>É possível observar abelhas ao redor da entrada no pito de entrada estas são as abelhas 
                        soldados, elas têm um corpo adaptado para o combate e raramente exercem outras funções 
                        na colônia. </p>
                </div>

